public class User {
	private int id;
	private String firstName;
	private String lastName;
	
	
	
	 int getId() {
		return id;
	}
	 void setId(int id) {
		this.id = id;
	}
	 String getFirstName() {
		return firstName;
	}
	 void setFirstName(String firstName) {
		this.firstName = firstName;
	}
	 String getLastName() {
		return lastName;
	}
	 void setLastName(String lastName) {
		this.lastName = lastName;
	}
}
